// number
var x = 10.6;
console.log(typeof x);

// big int
var x = 1000n;
console.log(typeof x);

// string
var x = 'Tutan';
console.log(typeof x);

// boolean
var x = true;
console.log(typeof x);

// undefined
var x;
console.log(typeof x);

// objects
var obj = {};
console.log(typeof obj);

var arr = [];
console.log(arr);

// symbol
var sym = Symbol('id');
console.log(typeof sym);

// null 
var x = 10;
x = null;
console.log(typeof x);